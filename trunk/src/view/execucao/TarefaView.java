    private ListSelectionListener selectionListener = new ListSelectionListener()
    {
        public void valueChanged(ListSelectionEvent e)
        {
            int linha = jtbpacotes.getSelectedRow();
            if(linha == -1)
            {
                jtbprocessos.setModel(new DefaultTableModel());
                return;
            }
            Pacote pacote;
            pacote = new PacoteData().getPacoteId(Integer.valueOf(String.valueOf(jtbpacotes.getValueAt(linha,0))));
            preencheTable(jtbprocessos, "select a.idprocesso as 'C√≥digo', a.nome as 'Nome' from processo a inner join  pacote_processo b on a.idprocesso = b.idprocesso where idpacote = "+ pacote.getId() + " and status = 1" );
        }
    };


