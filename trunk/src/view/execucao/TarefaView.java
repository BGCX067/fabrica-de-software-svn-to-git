    private void montaPacotes()
    {
        Grupo grupo;
        ArrayList<Grupo> grupos = manipulaConexao.getGrupoLogado();
        String sql = "";
        for(int i = 0; i < grupos.size(); i++)
        {
            grupo = grupos.get(i);
            if(i == 0)
            {
                sql = "where b.idgrupo = "+ grupo.getId();

            }
            else
            {
                sql = sql + " or b.idgrupo = "+ grupo.getId();

            }
        }
        preencheTable(jtbpacotes, "select distinct a.idpacote as 'C√≥digo Pacote' , a.descricao as 'Descri√ß√£o' from pacote a inner join grupo_pacote b on a.idpacote = b.idpacote " +sql);
    }

